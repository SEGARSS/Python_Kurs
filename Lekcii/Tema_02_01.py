# Добавления данных в список с помощью функции append.
list_1 = [1,9]
print("Состоит из 2х эелементов",list_1)
print("========================")
list_1.append(8) # Добавили (8) в наш список, 3й элемент.
print("Состоит из 3х эелементов",list_1)
print("========================")
list_1.append(38) # Добавили 4й элемент.
print("Состоит из 4х эелементов",list_1)
print("========================")

