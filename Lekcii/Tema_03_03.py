'''
Значения по умолчанию для функции.
'''
def new_string(symbol, count=3):
    return symbol * count
print(new_string('!', 5)) # !!!!!
print(new_string('!')) # !!!
print(new_string(4)) # 12
'''
Данный пример хорошо показывает, как дынные, строки и значения можно сложить или провести другие
операции.
'''